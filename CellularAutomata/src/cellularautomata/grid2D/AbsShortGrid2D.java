package cellularautomata.grid2D;

public class AbsShortGrid2D implements ShortGrid2D {
	
	private ShortGrid2D source;
	
	public AbsShortGrid2D(ShortGrid2D source) {
		this.source = source;
	}

	@Override
	public short getValueAtPosition(int x, int y) {
		return (short) Math.abs(source.getValueAtPosition(x, y));
	}

	@Override
	public int getMinX() {
		return source.getMinX();
	}

	@Override
	public int getMaxX() {
		return source.getMaxX();
	}

	@Override
	public int getMinY() {
		return source.getMinY();
	}

	@Override
	public int getMaxY() {
		return source.getMaxY();
	}

}
